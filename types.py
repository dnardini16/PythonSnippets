var = "Davide"

print(type(var))

# OUTPUT

# <class 'str'>

var = 10

print(type(var))

# OUTPUT

# <class 'int'>

var = 10.7

print(type(var))

# OUTPUT

# <class 'float'>

var = ["Davide", "Alessandra"]

