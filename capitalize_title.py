# capitalize e title in Python

nome = "alan turing"

print(nome.capitalize())

# OUTPUT

# Alan turing

# commento al codice:
# .capitalize() converte solo il primo carattere in maiuscolo

nome = "alan turing"

print(nome.title())

# OUTPUT

# Alan Turing

# commento al codice:
# .title() converte il primo carattere di ogni parola in maiuscolo

