# aggiungere un elemento ad una lista in Python

lista = ["Italia", "Olanda"]

lista.append("Francia") # append

print(lista)

# OUTPUT

# ["Italia", "Olanda", "Francia"]

lista = ["Italia", "Olanda", "Francia"]

lista.insert(0,"Spagna") # insert

print(lista)

# OUTPUT

# [Spagna", "Italia", "Olanda", "Francia"]

# commento al codice:
# con .append() aggiungo l'elemento alla fine della lista
# con .insert() aggiungo un elemento specificando l'indice

