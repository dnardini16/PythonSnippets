# ordinare una lista in Python

frutti = ["Ananas", "Banana", "Kiwy", "Arancia", "Pera"]

frutti.sort()

print(frutti)

# OUTPUT

# ['Ananas', 'Arancia', 'Banana', 'Kiwy', 'Pera']

frutti.sort(reverse=True)

print(frutti)

# OUTPUT

# ['Pera', 'Kiwy', 'Banana', 'Arancia', 'Ananas']