# estrarre le chiavi in un dizionario in Python

dizionario = {'Italia': 'Roma', 'Olanda': 'Amsterdam', 'Portogallo': 'Lisbona', 'Francia': 'Parigi'}
lista_keys = list(dizionario.keys())

print(lista_keys)

# OUTPUT

# ['Italia', 'Olanda', 'Portogallo', 'Francia']

# estrarre i valori in un dizionario in Python

lista_values = list(dizionario.values())

print(lista_values)

# OUTPUT

# ['Roma', 'Amsterdam', 'Lisbona', 'Parigi']

